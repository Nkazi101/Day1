package ControlStatements;

public class ForLoops {


    //for(intialization; condition; update(increment/decrement))
    public static void main(String[] args)
    {

        //use a for loop when there's a predetermined number of time you want a piece of code to execute
        for(int i = 1; i <= 5; i++){

            System.out.println(i);
        }


    }
   

}
